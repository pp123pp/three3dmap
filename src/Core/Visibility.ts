enum Visibility {
    /**
     * Represents that no part of an object is visible.
     *
     * @type {Number}
     * @constant
     */
    NONE = -1,

    /**
     * Represents that part, but not all, of an object is visible
     *
     * @type {Number}
     * @constant
     */
    PARTIAL = 0,

    /**
     * Represents that an object is visible in its entirety.
     *
     * @type {Number}
     * @constant
     */
    FULL = 1,
}
export default Visibility;
